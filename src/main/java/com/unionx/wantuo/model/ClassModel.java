package com.unionx.wantuo.model;


public class ClassModel {

    private Integer id;

    private String organizationAccounts;

    private String organizationClass;

    private String createDate;

    private String subject;

    private String student;

    private Integer status;

    private String statusName;
    
    private Integer num;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getOrganizationAccounts() {
		return organizationAccounts;
	}

	public void setOrganizationAccounts(String organizationAccounts) {
		this.organizationAccounts = organizationAccounts;
	}

	public String getOrganizationClass() {
		return organizationClass;
	}

	public void setOrganizationClass(String organizationClass) {
		this.organizationClass = organizationClass;
	}

	public String getCreateDate() {
		return createDate;
	}

	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}

	public String getStudent() {
		return student;
	}

	public void setStudent(String student) {
		this.student = student;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public String getStatusName() {
		return statusName;
	}

	public void setStatusName(String statusName) {
		this.statusName = statusName;
	}

	public Integer getNum() {
		return num;
	}

	public void setNum(Integer num) {
		this.num = num;
	}
	
}